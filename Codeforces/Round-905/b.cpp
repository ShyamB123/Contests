void solve()
{
    ll n,k;
    cin>>n>>k;

    ll a[n];

    ll ans = inf;
    for(int i=0;i<n;i++)
    {
        cin>>a[i];

        if(a[i]%k == 0)
        {
            ans = min(ans,0ll);
        }
        else
        {
            ll rem = k - a[i]%k;
            ans = min(ans,rem);
        }
    }

    if(k == 4)
    {
        int mds[4] = {0};
        for(int i= 0;i<n;i++)
        {   
            mds[a[i]%4]++;
        }

        if(mds[2] > 1 || mds[0] > 0)
        {
            ans = 0;
        }
        if(mds[2] == 1 && mds[1] > 0)
        {
            ans = min(ans,1ll);
        }
        if(mds[1] > 1)
        {
            ans = min(ans,2ll);
        }


    }

    cout << ans <<nl;
}


